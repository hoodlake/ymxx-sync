DECLARE
  VN_COUNT NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO VN_COUNT
    FROM ERP_ADDITIONAL_DEFINE
   WHERE FIELD_CODE='VENDOR_NUM';
  IF VN_COUNT < 1 THEN
	--初始化数据
	insert into ERP_ADDITIONAL_DEFINE(ID,FIELD_CODE,FIELD_NAME,IS_ADDITIONAL,IS_NOT_NULL)values(2,'VENDOR_NUM','客户编号','1','0');
  END IF;
  COMMIT;
END;
/
