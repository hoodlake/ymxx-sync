DECLARE
  VN_COUNT NUMBER;
BEGIN
  SELECT COUNT(*) INTO VN_COUNT FROM ERP_VOUCHER_TYPE WHERE ID = 4;
  IF (VN_COUNT < 1) THEN
    INSERT INTO ERP_VOUCHER_TYPE
      (ID, VOUCHER_TYPE_CODE, VOUCHER_TYPE_NAME)
    VALUES
      (4, '04', '承兑汇票');
  END IF;
  COMMIT;
END;
